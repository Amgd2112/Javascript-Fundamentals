let x=8, y=4;

console.log(x+y);
console.log(x-y);
console.log(x/y);
console.log(x*y);
console.log(x%y);
console.log(x**y);

console.log(x++*y);// operate first:8*4, 8+1
console.log(++x*y);// increament first:(9+1)*4

console.log(x--%y);// operate first: 10%4, (10-1)
console.log(--x%y);// decreament first: (9-1)%4